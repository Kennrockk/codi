let cont = 0;
while (cont <= 10) {
  console.log("repetición número:" + cont);
  cont++;
}
console.log("Valor final:" + cont);

for (let cont = 0; cont <= 10; cont++) {
  console.log("repetición número:" + cont);
}

/* bucle for o estructura de control for */
for (let conta = 0; conta <= 8; conta++) {
  console.log(`Iteración número: ${conta}`);
}
//console.log(`Valor final: &{conta}`);

/* Estructura do while */
let cont2 = 0;
do {
  console.log("repetición número:" + cont2);
  cont2++;
} while (cont2 <= 10);
console.log("Valor final:" + cont2);
